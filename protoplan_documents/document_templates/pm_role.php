<h1>Context</h1>
The Project Manager is the individual ultimately responsible for the project and is supported by the Senior Supplier and the Senior User.  The Executives role is to lead the Board in the meeting of the project’s objectives and benefits as shown in the Business Case, for which they are responsible.  Key is balancing the varying demands of the business, supplier and user but the Project Board isn’t a democracy.  The Executive should look for inputs and support from the Senior User and Senior Supplier but it is they who are the ultimate decision maker.

