  <li>	Select the project management team (in particular the Project manager)</li>
 <li>	Oversee the development of the Project Brief and Business Case, including ensuring this is aligned with any relevant corporate strategies (if required also managing the approval of this with programme or corporate management)</li>
  <li>	Ensure that sufficient funding is available and secured for the project</li>
