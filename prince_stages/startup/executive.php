Overview
<p>Using the Project Mandate and agreed Project Manager Job Description(s) appoint the Executive (who will make the decisions) and Project Manager (who will plan and manage the project). Upon their acceptance and sign off of their Job Description(s) appoint them to their roles.</p>

<strong>So have we got a clear Project Mandate, a clear Executive job description?</strong>




These are the roles of people on a project.


<p>The Executive is the ultimate decision-maker in the project and is responsible for the project delivering its products. For this reason the Executive is appointed in the pre-project sub process Quality Review. The appointment of the Executive (along with the Project Manager) is required before planning the Initiation Stage Plan. As the Executive is responsible for the Business Case it is expected that in most cases the Executive is from the Customer. Along with the Senior User and Senior Supplier the Executive forms the Project Board. The Executive appoints these roles and the roles of the Project Management Team.</p>

<p>During SU Starting up a Project a major output is the Project Brief. The Executive is responsible for its production as it forms the basis for the Project Initiation Document (PID). Once the projects has started the Executive is then responsible for:</p>


<ul>
<li>Development and continuation of the project's <a href="/prince2/?title=Business_Case" title="Business Case">Business Case</a>
</li><li>Ensuring that the project organisational structure and plans are consistent to achieve from the outset the project's goals. 
</li><li>Monitoring and control of process at the strategic level 
</li><li>Any problems are referred to, if necessary, <a href="/prince2/?title=Programme_Management" title="Programme Management">Programme Management</a>.
</li><li>Formally closing the project, confirming that aims and objectives have been met and that lessons learned have been documented. 
</li><li>Overseeing the <a href="/prince2/?title=Post_Project_Review" title="Post Project Review" class="mw-redirect">Post Project Review</a>, as to make sure that it takes place and it forwarded to the correct people. 
</li></ul>
<p>Some other key responsibilities include: 
</p>
<ul><li>Authorise <a href="/prince2/?title=Customer" title="Customer">Customer</a> expenditure and set stage <a href="/prince2/?title=Tolerances" title="Tolerances">Tolerances</a>
</li><li>Ensure the effect of any changes to cost, time-scales or scope are checked against the <a href="/prince2/?title=Business_Case" title="Business Case">Business Case</a>
</li><li>Ensure that risks are being tracked and dealt with appropriately 
</li><li>Monitor project finance on behalf of the <a href="/prince2/?title=Customer" title="Customer">Customer</a>
</li><li>Monitor <a href="/prince2/?title=Supplier" title="Supplier">Supplier</a> and contractor payments.
</li></ul>
<div id="executive_tasks">

<ol><li> Project Manager Performance—Examine the performance of that person.
Look for what else could be done or provided to make the project manager
successful.</li>
<li> Project Team Dynamics—Determine if the team members are getting along
and focused on the right things.</li>
<li> Project Approach and Outcomes—Learn the current results, cost, schedule
and risks – and possible tradeoffs between those factors.</li>
<li>Client Expectations—Uncover current client expectations and how to manage
them in the face of changing reality. Learn what non-IT issues will make or
break the business result of this project, and determine if the executive Client
(Q93) is attending to those issues.</li>
<li>Stakeholder Commitment—Determine the most recent stakeholder issues
that need to be resolved or exploited.</li></ol>
</div>

